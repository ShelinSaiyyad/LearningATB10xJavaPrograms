package ex_02_Literals;

import org.w3c.dom.ls.LSOutput;

public class Lab032_Literals_for_different_No_System {
    public static void main(String[] args) {


    // Binary Literal - base wil 2
    int binary_num = 0b1010; //10
    System.out.println(binary_num);


    // Octal Literal - base wil 8
    int octal_num = 0101; //65
    System.out.println(octal_num);

    // Decimal Literal - base wil 10
    int decimal_num = 65; //65
    System.out.println(decimal_num);

    // Hexadecimal Literals - base wil 16
    int hex_num = 0Xface; //64206
    System.out.println(hex_num);
    //base 16 color combination are in hex - #28a745

    // By using 'rapidtable.com' can check conversion easily..:)

    }

}
