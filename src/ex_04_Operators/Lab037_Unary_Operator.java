package ex_04_Operators;

public class Lab037_Unary_Operator {
    public static void main(String[] args) {

        // Unary Operator

        int a = +10;
        int b = -10;
        System.out.println(a);
        System.out.println(b);


        int a1 = 1;
        a1 = a1 - 1;
        System.out.println(a1);

        // a,b,a1 are operands
        // = is operator


    }
}
