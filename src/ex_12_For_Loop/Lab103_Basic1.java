package ex_12_For_Loop;

public class Lab103_Basic1 {
    public static void main(String[] args) {
        // For Loop - Repeat something...
        // Help you to repeat a block of code.
        // Initialization -> Condition -> Updation(Incre/decre) - ICU

        for(int i=0; i<10; i++) {
            System.out.println(i);
        }
    }
}
