package ex_12_For_Loop;

public class Lab105_Interview1 {
    public static void main(String[] args) {

        //condition got false at 1st run, so end the code
        for (int i = 0; i > 1 ; i++) {
            System.out.println(i);
        }
        System.out.println("End");

        for (int i = 0; i > 0 ; i--) {
            System.out.println(i);
        }
    }
}
